import numpy as np


class DirichletDistribution:
    ####################################################################################################################
    ### Constructor:
    ####################################################################################################################

    def __init__(self):
        self.alpha = 0.5

    ####################################################################################################################
    ### Encapsulation methods:
    ####################################################################################################################

    def set_alpha(self, alpha):
        self.alpha = alpha

    ####################################################################################################################
    ### Public methods:
    ####################################################################################################################

    def sample(self):
        raise RuntimeError('The "sample" method is not implemented yet for the Dirichlet distribution.')
        pass

    def compute_log_likelihood(self, observation):
        return np.prod(observation)**self.alpha
